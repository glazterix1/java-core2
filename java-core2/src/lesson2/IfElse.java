package lesson2;

public class IfElse {
    public static void main(String[] args) {
        int number = 0;
        if (number >= 0) {
            System.out.println("10 больше нуля");
        }

        if (number == 0) {
            System.out.println("number равен нуль");
        } else {
            System.out.println("10 меньше нуля");
        }
    }
}
